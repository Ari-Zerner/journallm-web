export function ThemeToggle() {
  const [mounted, setMounted] = useState(false);
  const { theme, setTheme } = useTheme();

  useEffect(() => {
    setMounted(true);
  }, []);

  const toggleTheme = () => {
    setTheme(theme === "light" ? "dark" : "light");
  };

  // Prevent hydration mismatch
  if (!mounted) {
    return <div className="w-14 h-7" />;
  }

  const isDark = theme === "dark";

  return (
    <button
      onClick={toggleTheme}
      className="relative inline-flex h-7 w-14 items-center rounded-full transition-colors bg-neutral-200 dark:bg-neutral-700"
      role="switch"
      aria-checked={isDark}
      aria-label={`Switch to ${isDark ? "light" : "dark"} mode`}
    >
      <span
        className={`
          inline-flex h-5 w-5 items-center justify-center rounded-full bg-white shadow-sm transition-transform duration-200
          ${isDark ? "translate-x-8" : "translate-x-1"}
        `}
      >
        {isDark ? (
          <MoonIcon className="h-3 w-3 text-neutral-700" />
        ) : (
          <SunIcon className="h-3 w-3 text-amber-500" />
        )}
      </span>
    </button>
  );
}
